# CTRL + / - comment
# CTRL + D - duplicate

# строка, string, str: "Hello, world!"
# целые числа, integer, int: 123
# дробные числа, float, float: 44.12
# булевые, boolean, bool: True/False


a = "Hello World!"
type_of_a = type(a)
print(a, type_of_a)

b = 123
type_of_b = type(b)
print(b, type_of_b)

c = 44.12
type_of_c = type(c)
print(c, type_of_c)

d = True
type_of_d = type(d)
print(d, type_of_d)


