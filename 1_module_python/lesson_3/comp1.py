# операторы сравнения
# <, >, <=, <=, ==, !=

print("12 == 12 -> ", 12 == 12)  # 12 равно ли 12

print("12 != 12 -> ", 12 != 12)  # 12 не равно ли 12

print("10 > 8 -> ", 10 > 8)  # 10 больше ли 8

print("10 < 8 -> ", 10 < 8)  # 10 меньше ли 8

print("12 <= 12 -> ", 12 <= 12)  # 12 меньше или равно 12? True

print("10 >= 12 -> ", 10 >= 12)  # 10 больше или равно 12? False

